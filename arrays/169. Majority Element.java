class Solution {
    public int majorityElement(int[] nums) {
        int count = 0;
        int candidate = 0;

        for (int num : nums) {
            if (count == 0) {
                candidate = num;
            }
            count += (num == candidate) ? 1 : -1;
        }

        return candidate;
    }
}
// Time Complexity: O(n) - Single pass through the array.
// Space Complexity: O(1) - Boyer-Moore Voting Algorithm uses constant space.
